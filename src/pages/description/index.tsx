import axios from "axios";
import { useRouter } from "next/router";
import { Fragment, useEffect, useState } from "react";
import * as M from "@mui/material";
import Head from "next/head";
import { usePiece } from "@/global/Provider/context";
import NavBar from "@/global/components/NavBar";
import CardInfos from "@/global/components/CardInfos";
import CardFuit from "@/global/components/FruitInfo";

export default function Description() {
  const [data, setData] = useState<any>([]);
  const router = useRouter();
  const { id } = router.query;
  const { personagemAtual } = usePiece();


  useEffect(() => {
    axios
      .get(
        `https://one-piece-br-default-rtdb.firebaseio.com/characters.json/${personagemAtual}`,
        {
          headers: {},
        }
      )
      .then((response: { data: any }) => {
        setData(response.data);
      })
      .catch(function (error: { toJSON: () => any }) {
        console.log(error.toJSON());
      });
  }, [id, personagemAtual]);

  console.log(data, "description");
  console.log(personagemAtual, "personagemAtual");

  const filterOne =
    data &&
    Object.values(data).filter((character: any) => {
      return character.id === 2;
    });

  // const found = resposta?.find((value: any) => {
  //   return value.id === 2;
  // });

  console.log(filterOne, "filterOne");
  
  return (
    <Fragment>
      <Head>
        <title>One Piece | Descriptions</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <NavBar />
      {filterOne.map((item: any) => {
        return (
            <Fragment key={item.id}>
            <CardInfos data={item}  />
            <CardFuit data={item}/>
            </Fragment>
        );
      })}
    </Fragment>
  );
}
