import axios from "axios";
import { useRouter } from "next/router";
import { Fragment, useEffect, useState } from "react";
import * as M from "@mui/material";
import Head from "next/head";
import { usePiece } from "@/global/Provider/context";
import NavBar from "@/global/components/NavBar";
import CardInfos from "@/global/components/CardInfos";
import CardFuit from "@/global/components/FruitInfo";

export default function Description() {
  const [data, setData] = useState<any>([]);
  const router = useRouter();
  const { id } = router.query;
  const { personagemAtual } = usePiece();

  useEffect(() => {
    axios
      .get(
        `https://one-piece-br-default-rtdb.firebaseio.com/characters/${id}.json/`,
        {
          headers: {},
        }
      )
      .then((response: { data: any }) => {
        console.log(response, "description");
        setData(response.data);
      })
      .catch(function (error: { toJSON: () => any }) {
        console.log(error.toJSON());
      });
  }, [id, personagemAtual]);

  return (
    <Fragment>
      <Head>
        <title>One Piece | Descriptions</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <NavBar />
      <Fragment >
        <CardInfos data={data} />
        <CardFuit data={data} />
      </Fragment>
    </Fragment>
  );
}
